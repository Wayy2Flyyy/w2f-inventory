local W = {}

local function weapon(name, label, weight, ammo, dura, extra)
    local def = { label = label, weight = weight, stack = false, durability = dura or 0.05 }
    if ammo then def.ammoname = ammo end
    if extra then for k, v in pairs(extra) do def[k] = v end end
    W[name] = def
end

local function ammo(name, label, weight)
    W[name] = { label = label, weight = weight or 0, stack = true, ammoType = true }
end

local function comp(name, label, ctype)
    W[name] = { label = label, weight = 50, stack = false, type = ctype }
end

